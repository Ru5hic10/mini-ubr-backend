import com.miniuber.core.driver.entity.DriverEarnings;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Transactional;

import java.time.LocalDateTime;
import java.util.List;

@Repository
public interface DriverEarningsRepository extends JpaRepository<DriverEarnings, Long> {

    List<DriverEarnings> findByDriverIdAndCreatedAtBetween(Long driverId, LocalDateTime start, LocalDateTime end);

    Page<DriverEarnings> findByDriverIdOrderByCreatedAtDesc(Long driverId, Pageable pageable);

    List<DriverEarnings> findByDriverIdAndCreatedAtAfterOrderByCreatedAtDesc(Long driverId, LocalDateTime after);

    @Transactional
    @Modifying
    @Query("UPDATE DriverEarnings e SET e.status = :status, e.paidAt = :paidAt, e.updatedAt = CURRENT_TIMESTAMP WHERE e.id = :earningsId")
    void updateEarningsStatus(@Param("earningsId") Long earningsId,
                              @Param("status") String status,
                              @Param("paidAt") LocalDateTime paidAt);
}
